public class SwitchCase {
	public static void main(String[] args){
		int a = 3;
		switch(a){
			case 1 :
				System.out.println("Satu");
				break;
			case 2 :
				System.out.println("Dua");
				break;
			case 3 : 
				System.out.println("Tiga");
				break;
			default :
				System.out.println("Bukan 1,2,dan 3");
		}	
	}
}
